class Person {

    //Data fields
    #name;
    #age;
    #gender;
    #height;
    #skill;

    
    constructor(name, age, gender, height, skill) {
        this.#name = name;
        this.#age = age;
        this.#gender = gender;
        this.#height = height;
        this.#skill = skill;
    }
}